// Helper function to get random questions from a module
export function getRandomQuestions(moduleId: string, count: number): QuizQuestion[] {
  const questions = qubitsQuestionBank[moduleId] || [];
  const shuffled = [...questions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, questions.length));
}

// Helper function to get questions from multiple modules
export function getQuestionsFromModules(
  moduleIds: string[],
  questionsPerModule: Record<string, number>
): QuizQuestion[] {
  const allQuestions: QuizQuestion[] = [];

  moduleIds.forEach((moduleId) => {
    const count = questionsPerModule[moduleId] || 10;
    const questions = getRandomQuestions(moduleId, count);
    allQuestions.push(...questions);
  });

  // Shuffle all questions
  return allQuestions.sort(() => Math.random() - 0.5);
}
